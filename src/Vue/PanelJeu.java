    public void addMondes(int nbMondes){
        //Methodes pour ajouter le nobre de mondes(mondes = boutons)
        int largeur = this.getWidth();
        int hauteur = this.getHeight()/2;
        int posiInit= largeur/(nbMondes+1);
        int i,posiObj;
        posiObj=posiInit;
        for(i=0;1<nbMondes;i++){
            JButton bouton = new JButton();
            bouton.setLocation(posiObj, hauteur);
            this.add(bouton);
            mondes.add(bouton);
        }
    }

